import java.util.Scanner;

public class Square08 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("Enter value N = ");
        int N = sc.nextInt();

        for (int iouter = 0; iouter <= N; iouter++) {
            for (int i = 0; i <= N; i++) {
                 System.out.print("*");
            }
       
    }
    
    }
}